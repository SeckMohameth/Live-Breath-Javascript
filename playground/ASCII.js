// capital letters are lower on the ASCII table 
// examples below

if('A' >= 'd') {
  console.log(true)
} else {
  console.log(false)
}

// false because a is 97 and d is 100 on the table
if('a' >= 'd') {
  console.log(true)
} else {
  console.log(false)
}

// will return true
if('x' >= 'd') {
  console.log(true)
} else {
  console.log(false)
}
